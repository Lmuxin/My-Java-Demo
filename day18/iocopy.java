            //第二种方式: 从C盘读取一个字符,就往D盘写一个字符
            
              public  static void copy_2()
                {
                    FileWriter fw=null;
                    FileReader fr=null;
                    try
                    {
                      fw=new FileWriter("SystemDemo_copy.txt");
                      fr=new FileReader("SystemDemo.java");    
                      
                      char [] buf=new char[1024];
                       
                      int len=0;
                        while((len=fr.read(buf))!=-1)  
                        {
                            fw.write(buf,0,len);
                        
                        }         
                    }
                
                catch(IOException e)
                  {
                   throw new RuntimeException("读写失败");
                  }
                  finally
                  {
                        if(fr!=null)
                        try
                        {
                          fr.close();
                          
                         }
                        catch(IOException e)
                        {
                        
                        }
                        
                        if(fw!=null)
                        try
                        {
                          fw.close();
                          
                          }
                        catch(IOException e)
                        {
                        
                        }
      
                        
                  }
                
   
                }
      
